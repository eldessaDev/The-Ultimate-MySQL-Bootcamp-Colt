    -- HAVING
    SELECT TITLE, avg(RATING), count(rating) as Review_Count
    FROM FULL_REVIEWS
    GROUP BY TITLE
    HAVING count(RATING) > 1;

    -- WITH ROLLUP
    SELECT TITLE, avg(RATING), count(rating) as Review_Count
    FROM FULL_REVIEWS
    GROUP BY TITLE
    HAVING count(RATING) > 1;

    SELECT TITLE, AVG(RATING) 
    FROM FULL_REVIEWS
    GROUP BY TITLE WITH ROLLUP;

    SELECT RELEASED_YEAR, GENRE, AVG(RATING)
    FROM FULL_REVIEWS
    GROUP BY RELEASED_YEAR, GENRE WITH ROLLUP;

