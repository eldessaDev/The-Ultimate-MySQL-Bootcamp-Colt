-- VIEWS
    -- CREATE VIEW
    CREATE VIEW full_reviews AS 
    SELECT s.TITLE, s.RELEASED_YEAR, s.GENRE, r.RATING, re.FIRST_NAME, re.LAST_NAME
    FROM REVIEWS r 
    JOIN SERIES s ON s.id = r.SERIE_ID
    JOIN REVIEWERS re ON re.ID = r.reviewer_ID;

    CREATE VIEW ordered_series AS
    SELECT* FROM SERIES
    ORDER BY RELEASED_YEAR;

